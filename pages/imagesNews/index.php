    <div style="float: right; margin-left: 4px;"> <img src="index_fichiers/podiumobelix.jpg" height="338" width="450"></div>
    <div>Nous
avons remporté l'Obelix Trophy. Bénodet était jusqu'à ce week-end la
seule régate du Trophée Atlantique où nous n'étions pas rentrés dans
les cinq au général. Nous sommes donc doublement contents. Par
ailleurs, cette seconde victoire n'a pas été facile. Même si nous
terminons avec 10 points d'avance, Super U pouvait encore gagner au
départ de la dernière manche. Nous avons eu de très bonnes sensations
de vitesse dans les conditions légères de ce week-end. Les parcours
bananes très courts favorisaient les bateaux qui trouvaient la vitesse
rapidement.<br>
      <br>
C'est vraiment agréable d'enchainer les manches comme le Comité de
Course l'a permis. Comme souvent, il a réussi à lancer autant de
courses qu'au Spi en ayant un jour de compétition en moins.<br>
<br>
Notre prochaine régate se joue à domicile. Gagner encore une fois ? Je
ne le crois pas, le niveau du groupe de tête est trop homogène pour
qu'un bateau puisse se détacher durablement. Notre équipage va aussi
tourner un peu comme entre le Spi et l'Obelix. Un podium nous
suffirait.
<br>
